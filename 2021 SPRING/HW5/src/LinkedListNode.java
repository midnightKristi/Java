public class LinkedListNode {
    private Object data;
    private LinkedListNode next;

    /////////////////////////////////////////////////////////////////////////////
    // NAME: LinkedListNode
    // BEHAVIOR: Constructs an LinkedListNode Object
    // PARAMETERS: data
    // RETURNS: nothing
    /////////////////////////////////////////////////////////////////////////////
    public LinkedListNode(Object data) {
        this.data = data;
    }

    /////////////////////////////////////////////////////////////////////////////
    // NAME: getNext
    // BEHAVIOR: Returns the next node of this node
    // PARAMETERS: none
    // RETURNS: next
    /////////////////////////////////////////////////////////////////////////////
    public LinkedListNode getNext() {
        return next;
    }

    /////////////////////////////////////////////////////////////////////////////
    // NAME: setNext
    // BEHAVIOR: Sets the next node of this node
    // PARAMETERS: newNext
    // RETURNS: nothing
    /////////////////////////////////////////////////////////////////////////////
    public void setNext(LinkedListNode newNext) {
        next = newNext;
    }

    /////////////////////////////////////////////////////////////////////////////
    // NAME:
    // BEHAVIOR: Returns the data object
    // PARAMETERS: none
    // RETURNS: data
    /////////////////////////////////////////////////////////////////////////////
    public Object getData() {
        return data;
    }

    /////////////////////////////////////////////////////////////////////////////
    // NAME: setData
    // BEHAVIOR: Sets the data object
    // PARAMETERS: newNext
    // RETURNS: nothing
    /////////////////////////////////////////////////////////////////////////////
    public void setData(Object data) {
        this.data = data;
    }

} // end of class
