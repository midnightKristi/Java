// Customer Record
public class CustomerRecord {
    private int customerNumber;
    private String firstName;
    private String lastName;
    private double balance;

    public CustomerRecord() {
        this.customerNumber = 0;
        this.firstName = "";
        this.lastName = "";
        this.balance = 0.0;
    }

    public CustomerRecord(int customerNumber, String firstName, String lastName, double balance) {
        this.customerNumber = customerNumber;
        this.firstName = firstName;
        this.lastName = lastName;
        this.balance = balance;
    }

    /////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //NAME: getCustomerNumber
    //BEHAVIOR: scans file for customer number and returns it
    //PARAMETERS: none
    //RETURNS: customer number
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public int getCustomerNumber() {
        return customerNumber;
    }

    /////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //NAME: setCustomerNumber
    //BEHAVIOR:
    //PARAMETERS:
    //RETURNS: nothing
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public void setCustomerNumber(int customerNumber) {
        this.customerNumber = customerNumber;
    }

    /////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //NAME: getFirstName
    //BEHAVIOR: scans file for customers first name and returns it
    //PARAMETERS: none
    //RETURNS: firstName
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public String getFirstName() {
        return firstName;
    }

    /////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //NAME: setFirstName
    //BEHAVIOR:
    //PARAMETERS:
    //RETURNS: nothing
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public void setFirstName(String firstName) {
        this.firstName = firstName;
    }

    /////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //NAME: getLastName
    //BEHAVIOR: scans file for customers last name and returns it
    //PARAMETERS: none
    //RETURNS: lastName
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public String getLastName() {
        return lastName;
    }

    /////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //NAME: setLastName
    //BEHAVIOR:
    //PARAMETERS:
    //RETURNS: nothing
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public void setLastName(String lastName) {
        this.lastName = lastName;
    }

    /////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //NAME: getBalance
    //BEHAVIOR: scans file for customers balance and returns it
    //PARAMETERS: none
    //RETURNS: balance
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public double getBalance() {
        return balance;
    }

    /////////////////////////////////////////////////////////////////////////////////////////////////////////////
    //NAME: setBalance
    //BEHAVIOR:
    //PARAMETERS:
    //RETURNS: nothing
    ////////////////////////////////////////////////////////////////////////////////////////////////////////////
    public void setBalance(double balance) {
        this.balance = balance;
    }

    public String toString() {
        return (customerNumber + " " + firstName + " " + lastName + " " + balance);
    }
}
